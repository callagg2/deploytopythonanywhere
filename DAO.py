    # deleting data from a database
    def delete_book(self, bookid):
        book_to_be_deleted= self.find_book_by_id(bookid)
        mycursor = self.get_cursor()
        sql = "Delete from books1 where id = %s"
        values = (bookid,)
        mycursor.execute(sql, values) # this passes the sql string and the values tuple to the execute function, which will insert the new record into the database. The %s in the sql string are placeholders for the values that will be passed in the values tuple. This is a common way to prevent SQL injection attacks, as it ensures that the values are properly escaped before being inserted into the database.
        self.connection.commit() # this is necessary to save the deletion to the database, if you don't call commit, the changes will not be saved and will be lost when the connection is closed.

        #print("1 record deleted")
        self.close_all()
        return True, book_to_be_deleted # we can return the book that was just deleted, so that we can see what was deleted in the test output

    def convert_to_dict(self, resultline):
        bookkeys = ["id", "title", "author", "price"]
        current_key = 0
        book = {}
        for attrib in resultline:
            book[bookkeys[current_key]] = attrib
            current_key += 1
        return book
        

